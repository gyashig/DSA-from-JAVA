import java.util.*;
public class si 
{
     public static void main(String[] args) 
    {
        Scanner sc=new Scanner(System.in);
        double pi=sc.nextDouble();
        float r=sc.nextFloat();
        int t=sc.nextInt();
        double sip=(pi*r*t)/100;
        System.out.println("Si is "+sip);
    }
}
